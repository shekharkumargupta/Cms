package com.opgea.cms.web.dto;

/**
 *
 * @author Shekhar
 */
public class DocumentDTO {

    
    private Long id;
    private Long resumeId;
    private Integer documentTypeId;
    private String documentTypeName;
    private String fileName;
    private String fileType;
    private Long fileSize;
    private String updatedAt;
    private Long uploadedById;
    private Long branchId;
    private Long companyId;

    public Integer getDocumentTypeId() {
        return documentTypeId;
    }

    public void setDocumentTypeId(Integer documentTypeId) {
        this.documentTypeId = documentTypeId;
    }

    public String getDocumentTypeName() {
        return documentTypeName;
    }

    public void setDocumentTypeName(String documentTypeName) {
        this.documentTypeName = documentTypeName;
    }

    public String getFileName() {
        return fileName;
    }

    public void setFileName(String fileName) {
        this.fileName = fileName;
    }

    public Long getFileSize() {
        return fileSize;
    }

    public void setFileSize(Long fileSize) {
        this.fileSize = fileSize;
    }

    public String getFileType() {
        return fileType;
    }

    public void setFileType(String fileType) {
        this.fileType = fileType;
    }

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public Long getResumeId() {
        return resumeId;
    }

    public void setResumeId(Long resumeId) {
        this.resumeId = resumeId;
    }

    public String getUpdatedAt() {
        return updatedAt;
    }

    public void setUpdatedAt(String updatedAt) {
        this.updatedAt = updatedAt;
    }

    public Long getUploadedById() {
        return uploadedById;
    }

    public void setUploadedById(Long uploadedById) {
        this.uploadedById = uploadedById;
    }

    public Long getBranchId() {
        return branchId;
    }

    public void setBranchId(Long branchId) {
        this.branchId = branchId;
    }

    public Long getCompanyId() {
        return companyId;
    }

    public void setCompanyId(Long companyId) {
        this.companyId = companyId;
    }

    @Override
    public String toString() {
        return "DocumentDTO{" + "id=" + id + ", resumeId=" + resumeId + ", documentTypeId=" + documentTypeId + ", documentTypeName=" + documentTypeName + ", fileName=" + fileName + ", fileType=" + fileType + ", fileSize=" + fileSize + ", updatedAt=" + updatedAt + ", uploadedById=" + uploadedById + ", branchId=" + branchId + ", companyId=" + companyId + '}';
    }
    
    

  
    
    
}
