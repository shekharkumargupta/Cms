package com.opgea.cms.domain.entities;

import java.io.Serializable;
import javax.persistence.Entity;
import javax.persistence.GeneratedValue;
import javax.persistence.Id;
import javax.persistence.Lob;
import javax.persistence.NamedQueries;
import javax.persistence.NamedQuery;
import javax.persistence.Table;
import javax.xml.bind.annotation.XmlRootElement;

/**
 *
 * @author Ramesh
 */
@Entity
@Table(name = "resumedocument")
@XmlRootElement
@NamedQueries({
    @NamedQuery(name = "ResumeDocument.findById", query = "SELECT r FROM Resume r WHERE r.id = :id"),
    @NamedQuery(name = "ResumeDocument.findAll", query = "SELECT r FROM Resume r")}
)
public class ResumeDocument implements Serializable {
    
    @Id
    @GeneratedValue
    private Long id;
    @Lob
    private String content;


    public ResumeDocument() {
    }

    public String getContent() {
        return content;
    }

    public void setContent(String content) {
        this.content = content;
    }

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }
   

    @Override
    public boolean equals(Object obj) {
        if (obj == null) {
            return false;
        }
        if (getClass() != obj.getClass()) {
            return false;
        }
        final ResumeDocument other = (ResumeDocument) obj;
        if (this.id != other.id && (this.id == null || !this.id.equals(other.id))) {
            return false;
        }
        return true;
    }

    @Override
    public int hashCode() {
        int hash = 7;
        hash = 97 * hash + (this.id != null ? this.id.hashCode() : 0);
        return hash;
    }

    @Override
    public String toString() {
        return "ResumeDocument{" + "id=" + id + ", content=" + content + '}';
    }
    
    
}
